package android.example.com.newsolutionbodymanager.welcomingActivity;

public class setDashboard {
    public int dailyCalorie,consumedCalorie,burnedCalorie,currentWeight,Breakfast,Lunch,Dinner,Snack;


    public setDashboard(int dailyCalorie, int consumedCalorie, int burnedCalorie, int currentWeight, int breakfast, int lunch, int dinner, int snack) {
        this.dailyCalorie = dailyCalorie;
        this.consumedCalorie = consumedCalorie;
        this.burnedCalorie = burnedCalorie;
        this.currentWeight = currentWeight;
        this.Breakfast = breakfast;
        this.Lunch = lunch;
        this.Dinner = dinner;
        this.Snack = snack;
    }
}
